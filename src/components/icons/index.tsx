export interface IconProps {
  size?: string | number;
  color?: string;
  fill?: string;
}

export type IconType = keyof typeof IconComponent;

interface IconsProps extends IconProps {
  icon: IconType;
  size?: number;
}

const Icons = ({ icon, color, ...res }: IconsProps) => {
  const Icon = IconComponent[icon];
  // const theme = useTheme();
  const colorObj = "#ea580c #f97316 #dc2626 #000000 #ffffff".split(" ").reduce((acc: { [key: string]: string }, color) => {
    acc[color] = color;
    return acc;
  }, {});
  if (!Icon) return null;
  return <Icon {...res} color={colorObj[color || "#fffff"] || color} />;
};

export default Icons;
